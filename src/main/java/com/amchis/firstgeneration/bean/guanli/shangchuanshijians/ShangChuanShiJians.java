package com.amchis.firstgeneration.bean.guanli.shangchuanshijians;

public class ShangChuanShiJians {
    private Integer id;

    private String xuliehao;

    private String wenjianming;

    private String cishu;

    private Long kaishishijian;

    private Long jieshushijian;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getXuliehao() {
        return xuliehao;
    }

    public void setXuliehao(String xuliehao) {
        this.xuliehao = xuliehao;
    }

    public String getWenjianming() {
        return wenjianming;
    }

    public void setWenjianming(String wenjianming) {
        this.wenjianming = wenjianming;
    }

    public String getCishu() {
        return cishu;
    }

    public void setCishu(String cishu) {
        this.cishu = cishu;
    }

    public Long getKaishishijian() {
        return kaishishijian;
    }

    public void setKaishishijian(Long kaishishijian) {
        this.kaishishijian = kaishishijian;
    }

    public Long getJieshushijian() {
        return jieshushijian;
    }

    public void setJieshushijian(Long jieshushijian) {
        this.jieshushijian = jieshushijian;
    }
}